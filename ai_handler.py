class AIHandler:
    def __init__(self, api_key: str):
        self.client = genai.Client(api_key=api_key)
        self.model = "gemini-2.5-flash"

    async def _chat(self, prompt: str, max_tokens: int = 1500) -> str:
        """Send a prompt to Gemini with automatic retry on rate limit."""
        retries = 3
        backoff = 10  # seconds

        for attempt in range(retries):
            try:
                response = await self.client.aio.models.generate_content(
                    model=self.model,
                    contents=prompt,
                    config=types.GenerateContentConfig(
                        system_instruction=SYSTEM_PROMPT,
                        max_output_tokens=max_tokens,
                        temperature=0.75,
                    ),
                )
                return response.text.strip()

            except Exception as e:
                error_str = str(e)
                if "429" in error_str or "RESOURCE_EXHAUSTED" in error_str:
                    if attempt < retries - 1:
                        wait = backoff * (attempt + 1)
                        logger.warning(f"Rate limited. Retrying in {wait}s (attempt {attempt + 1}/{retries})")
                        await asyncio.sleep(wait)
                        continue
                    else:
                        logger.error(f"Gemini API error: {e}")
                        return RATE_LIMIT_MESSAGE
                else:
                    logger.error(f"Gemini API error: {e}")
                    raise

    async def answer_question(
        self,
        question: str,
        user_goals: Optional[List[Tuple]] = None,
    ) -> str:
        """Answer a financial question based on the course."""
        goals_context = ""
        if user_goals:
            goals_context = "\n\n[Пайдаланушының қазіргі мақсаттары:]\n"
            for g in user_goals[:3]:
                _, description, goal_amount, current_amount, deadline, _ = g
                pct = (current_amount / goal_amount * 100) if goal_amount > 0 else 0
                goals_context += f"• {description}: {current_amount:,.0f}/{goal_amount:,.0f} ₸ ({pct:.0f}%)\n"

        return await self._chat(question + goals_context)

    async def generate_goal_plan(
        self,
        description: str,
        amount: float,
        deadline: datetime,
    ) -> str:
        """Generate a step-by-step plan to achieve a financial goal."""
        days_left = (deadline - datetime.now()).days
        months_left = max(1, days_left // 30)
        monthly_needed = amount / months_left

        prompt = (
            f"Пайдаланушының қаржылық мақсаты:\n"
            f"• Мақсат: {description}\n"
            f"• Жалпы сома: {amount:,.0f} ₸\n"
            f"• Мерзімі: {deadline.strftime('%d.%m.%Y')} ({months_left} ай)\n"
            f"• Ай сайын жинақтау қажет: {monthly_needed:,.0f} ₸\n\n"
            f"Осы мақсатқа жету үшін курстың принциптері бойынша нақты, қадамдық жоспар жасаңыз. "
            f"Бюджет, жинақтау стратегиясы, және мотивация туралы айтыңыз. "
            f"Қай модульдер қолданылатынын атаңыз."
        )
        return await self._chat(prompt, max_tokens=1200)

